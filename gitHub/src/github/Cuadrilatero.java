package github;


public class Cuadrilatero {
    //atributos
    private double alto;
    private double largo;
    //Constructor

    public Cuadrilatero(double alto, double largo) {
        this.alto = alto;
        this.largo = largo;
    }

    public double getAlto() {
        return alto;
    }

    public void setAlto(double alto) {
        this.alto = alto;
    }

    public double getLargo() {
        return largo;
    }

    public void setLargo(double largo) {
        this.largo = largo;
    }
    
}
